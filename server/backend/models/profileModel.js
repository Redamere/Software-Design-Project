const mongoose = require('mongoose')

const Schema = mongoose.Schema

const profileSchema = new Schema({
    fullName: {
        type: String,
        required: true
    },
    address1: {
        type: String,
        required: true
    },
    address2: {
        type: String,
        required: false
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    zipcode: {
        type: String,
        required: true
    }
})

module.exports = mongoose.model("Profile", profileSchema)